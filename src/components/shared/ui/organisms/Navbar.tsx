import React, { useState } from 'react';
import { Link as RouterLink, useNavigate } from 'react-router-dom';
import {
  AppBar,
  Box,
  Toolbar,
  IconButton,
  Menu,
  MenuItem,
  Button,
  Container,
  Typography as MuiTypography
} from '@mui/material';
import MenuIcon from '@mui/icons-material/Menu';
import CloseIcon from '@mui/icons-material/Close';
import Brightness4Icon from '@mui/icons-material/Brightness4';
import Brightness7Icon from '@mui/icons-material/Brightness7';
import { useColorMode } from '../../../../context/ThemeContext';
import { useLanguage } from '../../../../context';
import { useColors } from '../../../../hooks';
import { Logo, LanguageToggle } from '../atoms';
import { ROUTES } from '../../../../utils/constants';
import { scrollToTop } from '../../../../utils/helpers';

const Navbar = () => {
  const [anchorElNav, setAnchorElNav] = useState<null | HTMLElement>(null);
  const { mode, toggleColorMode } = useColorMode();
  const { t } = useLanguage();
  const colors = useColors();
  const navigate = useNavigate();

  // Items de navegación con traducciones
  const navItems = [
    { name: t('nav.home'), path: ROUTES.HOME },
    { name: t('nav.services'), path: ROUTES.SERVICES },
    { name: t('nav.whyAI4U'), path: ROUTES.WHY_AI4U },
    { name: t('nav.successCases'), path: ROUTES.SUCCESS_CASES },
    { name: t('nav.gallery'), path: ROUTES.GALLERY }
  ];

  const handleOpenNavMenu = (event: React.MouseEvent<HTMLElement>) => {
    setAnchorElNav(event.currentTarget);
  };

  const handleCloseNavMenu = () => {
    setAnchorElNav(null);
  };

  const handleNavigate = (path: string) => {
    handleCloseNavMenu();
    navigate(path);
    scrollToTop();
  };

  return (
    <AppBar 
      position="fixed" 
      color="default" 
      elevation={1} 
      sx={{ 
        backgroundColor: colors.contrast.surface,
        borderBottom: `1px solid ${colors.contrast.border}`,
        zIndex: (theme) => theme.zIndex.drawer + 1 
      }}
    >
      <Container maxWidth="lg">
        <Toolbar disableGutters>
          {/* Logo - Desktop */}
          <Box
            component={RouterLink}
            to={ROUTES.HOME}
            sx={{
              mr: 3,
              display: { xs: 'none', md: 'flex' },
              textDecoration: 'none',
              alignItems: 'center',
            }}
          >
            <Logo variant="desktop" />
          </Box>

          {/* Mobile Menu */}
          <Box sx={{ flexGrow: 1, display: { xs: 'flex', md: 'none' } }}>
            <IconButton
              size="large"
              aria-label="Menu de navegación"
              aria-controls="menu-appbar"
              aria-haspopup="true"
              onClick={handleOpenNavMenu}
              sx={{
                color: colors.contrast.text.primary,
                '&:hover': {
                  backgroundColor: colors.helpers.state.hover,
                },
              }}
            >
              {anchorElNav ? <CloseIcon /> : <MenuIcon />}
            </IconButton>
            <Menu
              id="menu-appbar"
              anchorEl={anchorElNav}
              anchorOrigin={{
                vertical: 'bottom',
                horizontal: 'left',
              }}
              keepMounted
              transformOrigin={{
                vertical: 'top',
                horizontal: 'left',
              }}
              open={Boolean(anchorElNav)}
              onClose={handleCloseNavMenu}
              sx={{
                display: { xs: 'block', md: 'none' },
                '& .MuiPaper-root': {
                  backgroundColor: colors.contrast.surface,
                  border: `1px solid ${colors.contrast.border}`,
                  boxShadow: '0 8px 32px rgba(0,0,0,0.1)',
                },
              }}
            >
              {navItems.map((item) => (
                <MenuItem 
                  key={item.name} 
                  onClick={() => handleNavigate(item.path)}
                  component={RouterLink}
                  to={item.path}
                  sx={{
                    color: colors.contrast.text.primary,
                    '&:hover': {
                      backgroundColor: colors.helpers.state.hover,
                      color: colors.palette.orange,
                    },
                  }}
                >
                  <MuiTypography textAlign="center">{item.name}</MuiTypography>
                </MenuItem>
              ))}
              {/* Theme toggle en menú móvil */}
              <MenuItem 
                onClick={toggleColorMode}
                sx={{
                  color: colors.contrast.text.primary,
                  '&:hover': {
                    backgroundColor: colors.helpers.state.hover,
                    color: colors.palette.orange,
                  },
                }}
              >
                <MuiTypography textAlign="center" sx={{ display: 'flex', alignItems: 'center' }}>
                  {mode === 'dark' ? 'Modo Claro' : 'Modo Oscuro'}
                  <Box component="span" sx={{ ml: 1, display: 'flex' }}>
                    {mode === 'dark' ? <Brightness7Icon fontSize="small" /> : <Brightness4Icon fontSize="small" />}
                  </Box>
                </MuiTypography>
              </MenuItem>
            </Menu>
          </Box>

          {/* Logo - Mobile */}
          <Box
            component={RouterLink}
            to={ROUTES.HOME}
            sx={{
              mr: 2,
              display: { xs: 'flex', md: 'none' },
              flexGrow: 1,
              textDecoration: 'none',
              alignItems: 'center',
              justifyContent: 'center',
            }}
          >
            <Logo variant="mobile" />
          </Box>

          {/* Desktop Menu */}
          <Box sx={{ flexGrow: 1, display: { xs: 'none', md: 'flex' }, justifyContent: 'flex-end', alignItems: 'center' }}>
            {navItems.map((item) => (
              <Button
                key={item.name}
                onClick={() => scrollToTop()}
                component={RouterLink}
                to={item.path}
                sx={{ 
                  mx: 1,
                  color: colors.contrast.text.primary,
                  fontWeight: 500,
                  textTransform: 'none',
                  '&:hover': {
                    color: colors.palette.orange,
                    backgroundColor: colors.helpers.state.hover,
                  },
                }}
              >
                {item.name}
              </Button>
            ))}
            
            {/* Language toggle button desktop */}
            <Box sx={{ ml: 1 }}>
              <LanguageToggle variant="icon" size="small" />
            </Box>
            
            {/* Theme toggle button desktop */}
            <IconButton 
              onClick={toggleColorMode} 
              size="small"
              sx={{ 
                ml: 1,
                color: colors.contrast.text.secondary,
                opacity: 0.7,
                '&:hover': { 
                  opacity: 1,
                  backgroundColor: colors.helpers.state.hover,
                  color: colors.palette.orange,
                }
              }}
              aria-label={mode === 'dark' ? 'Cambiar a modo claro' : 'Cambiar a modo oscuro'}
            >
              {mode === 'dark' ? <Brightness7Icon fontSize="small" /> : <Brightness4Icon fontSize="small" />}
            </IconButton>
          </Box>
        </Toolbar>
      </Container>
    </AppBar>
  );
};

export default Navbar; 